/*Em Java, é importante seguir as convenções de nomenclatura para manter seu código legível e manutenível. Aqui estão algumas regras principais:

Nomes de Variáveis:
Use camelCase: Comece com uma letra minúscula, depois capitalize a primeira letra de cada palavra subsequente (p. ex., firstName, studentCount).
Escolha nomes descritivos que indiquem o propósito da variável (p. ex., userAge em vez de ua).
Evite nomes de letra única, exceto para contadores de loop simples.
Nomes de Constantes:
Use UPPER_SNAKE_CASE: Escreva em letras maiúsculas, com palavras separadas por underscores (p. ex., MAX_VALUE, PI_VALUE).
Use para valores que não mudam ao longo do programa.
Regras Gerais:
Os nomes podem conter letras, dígitos, underscores e sinais de dólar.
Os nomes devem começar com uma letra, um underscore _ ou um sinal de dólar $.
Os nomes são sensíveis a maiúsculas e minúsculas (myVariable é diferente de myvariable).
Evite usar palavras-chave reservadas do Java (como int, class, public, etc.).
Seguir essas convenções ajuda a tornar seu código mais compreensível, especialmente ao trabalhar em equipes ou revisitar seu código mais tarde.
*/