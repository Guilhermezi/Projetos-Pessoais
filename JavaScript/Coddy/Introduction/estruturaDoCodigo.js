/*O código JavaScript é executado de cima para baixo, linha por linha. 

Cada linha de código é um comando (também chamado de declaração) que diz ao computador o que fazer.*/

console.log("Hello!") // First command
console.log("How are you?") // Second command

/*Em JavaScript, você pode terminar cada comando com um ponto e vírgula (;), mas não é obrigatório. O computador entende seu código com ou sem ele.

Ambos os exemplos abaixo funcionam da mesma forma:*/
console.log("Semicolon is here!"); // With ;
console.log("No semicolon here!")  // Without ;
// Mas adicionar ponto e vírgula é um bom hábito para evitar erros!