/*Variáveis são contêineres que armazenam valores de dados. Elas são usadas para armazenar, manipular e exibir informações dentro de um programa.

Em resumo, uma variável é como uma unidade de memória que podemos acessar digitando o nome da variável. 

Cada variável tem um nome único e um valor que pode ser de diferentes tipos. O JavaScript é capaz de detectar automaticamente o tipo da variável, o que torna a codificação mais eficiente.

Para inicializar uma variável, usamos o seguinte formato:*/
let variable_name = value

/*Vamos dar uma olhada nos diferentes tipos de números:

O JavaScript tem um tipo number que pode representar tanto inteiros quanto números de ponto flutuante.

number - pode ser um número inteiro, como 1 ou -2, ou um número real, como 1.32 ou 0.98.

Por exemplo:

Para inicializar uma variável do tipo number com o nome a e o valor 3:*/
let a = 3
//Para inicializar uma variável do tipo number com o nome b e o valor 13.2:
