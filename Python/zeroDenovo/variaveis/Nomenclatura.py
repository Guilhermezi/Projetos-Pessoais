# Convenções de nomenclatura são um conjunto de diretrizes que os desenvolvedores seguem para tornar seu código mais legível e manutenível. Diferentes linguagens de programação frequentemente têm diferentes convenções de nomenclatura. No Python, variáveis são escritas em snake case - palavras são separadas por sublinhados.

# Ao escrever um nome de variável, seja descritivo e use palavras significativas

# Por exemplo 
# Bad Naming
isActive = False # not snake case
a = 10
b = "Hello"
x = True

# Good Naming
age = 10
greeting = "Hello"
is_active = True