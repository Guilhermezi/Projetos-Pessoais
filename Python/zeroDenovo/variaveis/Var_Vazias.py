# Em Python, None é um valor especial que representa "nada" ou "sem valor". É como uma caixa vazia - ela existe, mas não há nada dentro dela. Por exemplo:
empty_box = None

# Em um cenário real, você poderia usar None para indicar que algo ainda não foi inicializado.

# Por exemplo:

score = None  # A pontuação ainda não foi calculada
name = None   # O nome ainda não foi inserido 