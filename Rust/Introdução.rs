// Rust é uma linguagem de programação de sistemas moderna projetada com foco em desempenho, segurança de memória e concorrência. Ela capacita os desenvolvedores a criar software confiável e eficiente, desde sistemas operacionais até aplicações web.

/*O "Hello World!" é um programa simples que exibe Hello World! na tela.

Em Rust, usamos a macro println!() para imprimir saída no console. O texto a ser impresso é colocado entre aspas duplas e envolvido em parênteses.

Vamos dar uma olhada no programa "Hello World!" em Rust:*/

fn main() {
    println!("Hello, World!");
}

/*Em Rust, todo programa executável começa com a função main. Esta função é o ponto de entrada do programa, e é onde a execução inicia. A função main é definida usando a palavra-chave fn seguida de main e um par de parênteses (). O código a ser executado é colocado dentro de chaves {}.

Aqui está uma simples explicação de um programa Rust básico:*/

fn main() { // Função principal
    println!("Hello, Coddy!"); // Instrução de saída
}

/*Nota importante: Em Rust, cada declaração deve terminar com um ponto e vírgula (;). O ponto e vírgula é obrigatório e informa ao Rust que você chegou ao final de uma declaração. Esquecer de adicionar um ponto e vírgula resultará em um erro de compilação. No entanto, observe que blocos de código delimitados por chaves {} (como declarações de funções) não precisam de ponto e vírgula.*/